import fs from 'fs';
import path from 'path';

function ensureDir(dir) {
  if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });
}

function backupFile(targetPath) {
  const backupsDir = path.join(process.cwd(), 'scripts', 'backups');
  ensureDir(backupsDir);
  const base = path.basename(targetPath);
  const ts = Date.now();
  const dst = path.join(backupsDir, `${ts}-${base}.bak`);
  if (fs.existsSync(targetPath)) fs.copyFileSync(targetPath, dst);
  return dst;
}

function evalModuleExports(filePath, exportNames = []) {
  const raw = fs.readFileSync(filePath, 'utf8');
  // Convert 'export const X = ' into 'const X = ' so we can eval
  const transformed = raw.replace(/export\s+const\s+/g, 'const ');
  const module = {};
  const fn = new Function('module', 'exports', `${transformed}\nreturn { ${exportNames.join(', ')} };`);
  return fn(module, module.exports || {});
}

function generateFromEpicsAndStories(epics, stories) {
  const activities = epics.map((e, i) => ({
    id: e.id,
    title: e.title,
    description: e.description || '',
    order: i + 1,
    steps: [],
  }));
  const mapById = new Map(activities.map(a => [a.id, a]));

  const unmapped = { id: 'UNMAPPED', title: 'Unmapped', description: '', order: 9999, steps: [] };

  stories.forEach(s => {
    const step = { id: s.id, title: s.title, description: s.description || '', order: s.storyPoints || 0 };
    if (s.epicId && mapById.has(s.epicId)) {
      mapById.get(s.epicId).steps.push(step);
    } else {
      unmapped.steps.push(step);
    }
  });

  activities.forEach(a => a.steps.sort((x,y)=> (x.order||0)-(y.order||0)));
  if (unmapped.steps.length) unmapped.steps.sort((x,y)=> (x.order||0)-(y.order||0));

  const outcome = {
    id: `OUT-${Date.now()}`,
    title: 'Auto-generated story map (from epics & stories)',
    description: 'Generated by scripts/generate-initial-storymap.mjs',
    activities: activities.concat(unmapped.steps.length ? [unmapped] : []),
    order: 1,
  };

  return [outcome];
}

async function main() {
  const repoRoot = process.cwd();
  const epicsPath = path.join(repoRoot, 'src', 'app', 'data', 'initial-epics.ts');
  if (!fs.existsSync(epicsPath)) {
    console.error('initial-epics.ts not found at', epicsPath);
    process.exit(2);
  }

  const exports = evalModuleExports(epicsPath, ['initialEpics', 'initialUserStories']);
  const epics = exports.initialEpics || [];
  const stories = exports.initialUserStories || [];

  const storyMap = generateFromEpicsAndStories(epics, stories);

  const targetPath = path.join(repoRoot, 'src', 'app', 'data', 'initial-storymap.ts');
  const backup = backupFile(targetPath);
  const content = `import { StoryMap } from "../types/storymap";

export const initialStoryMap: StoryMap = ${JSON.stringify(storyMap, null, 2)};
`;
  fs.writeFileSync(targetPath, content, 'utf8');
  console.log('Backed up old file to:', backup);
  console.log('Wrote new story map to:', targetPath);
}

main().catch(err => { console.error(err); process.exit(3); });
